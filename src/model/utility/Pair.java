package model.utility;

public class Pair {
	
	private int x;
	private int y;
	
	public Pair() {
		this(0, 0);
	}
	
	public Pair(int x, int y) {
		set(x, y);
	}
	
	public void set(int x, int y) {
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
}
